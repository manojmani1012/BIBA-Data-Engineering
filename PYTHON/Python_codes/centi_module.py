# tempConversion.py to convert between Fahrenheit and Centigrade
# function to convert F to C
def to_centigrade(m):
      return 5 * (m - 32) / 9.0
 
# function to convert C to F
def to_fahrenheit(m):
       return 9 * m / 5.0 + 32
