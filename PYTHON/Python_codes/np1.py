import numpy as np 
arr = np.array( [[ 9, 10, 8], 
                 [ 7, 6, 5]] ) 
  
print("Array is of type: ", type(arr))                  #type of arr object 
  
print("No. of dimensions: ", arr.ndim)                  # array dimensions 
  
print("Shape of array: ", arr.shape)                    #  shape of array 
  
print("Size of array: ", arr.size)                      # total number of elements
  
print("Array stores elements of type: ", arr.dtype)     # type of elements in array 

