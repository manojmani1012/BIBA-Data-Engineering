#NumPy  Binary Operators
import numpy as np
a = np.array([[2, 4],[1, 3]]) 
b = np.array([[3, 2],[1, 4]]) 
  
print(a + b)                                       # add arrays 
    
print(a*b)                                           # multiply arrays (elementwise multiplication) 
  
print(a.dot(b))                                      # matrix multiplication 
